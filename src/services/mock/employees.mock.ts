const sk = (
  id: string,
  name: string,
  category: Skill['category'],
  level: Skill['level'],
  years: number,
  verified: boolean,
): Skill => ({
  id,
  name,
  category,
  level,
  yearsOfExperience: years,
  verified,
})

const al = (
  companyId: string,
  projectName: string,
  percent: number,
  startDate: string,
  endDate?: string,
  branch?: string,
  deadline?: string,
  effectiveness?: string,
) => {
  const c = companyById(companyId)
  return {
    companyId,
    companyName: c.name,
    projectName,
    percent,
    startDate,
    endDate,
    branch,
    deadline,
    effectiveness,
  }
}

